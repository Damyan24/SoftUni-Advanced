package DefiningClasses.OpinionPoll;

public class Person {

    private String name;
    private int age;


    public void setName(String name) {
        this.name = name;
    }

    public void setage(int age) {
        this.age = age;
    }

    public String getName(){
        return this.name;
    }

    public int getAge(){
        return this.age;
    }
}
